	function set()
	{
		$("#outlet").val('');
	}

	function edit(id)
	{	
		$("#terlambat_"+id).removeAttr('readonly');
		$("#absen_"+id).removeAttr('readonly');
		$("#sakit_"+id).removeAttr('readonly');
		$("#edit_"+id).attr('disabled','true');

	}

	function cek(){
		var periode = $("#periode").val();
		var outlet = $("#outlet").val();

		if (periode != '')
		{
			if (outlet != '')
			{
				// alert("ini ok");

				$.ajax({
					url:'<?php echo base_url(); ?>master/kehadiran_act/cek',
					type:'post',
					data : {'outlet': outlet,'periode':periode},
					success:function(r){
						// alert("ok");
						$("#data-kehadiran").html(r);

					},
					error:function(r){
						alert(r);

					}

				});
			}else{
				$("#outlet").focus();
				$("#data-kehadiran").html('');
			}
		}else{
			$("#outlet").val('');
			$("#periode").val('');
			$("#periode").focus();
		}
	}
	</script>